
// Basic math operations
console.log(Math.abs(-5));     // Absolute value → 5
console.log(Math.round(4.6));  // Round to nearest integer → 5
console.log(Math.floor(4.9));  // Round down → 4
console.log(Math.ceil(4.1));   // Round up → 5
console.log(Math.trunc(4.67)); // Remove decimal → 4

// Power and roots
console.log(Math.pow(2, 3));   // 2^3 = 8
console.log(Math.sqrt(25));    // Square root of 25 = 5
console.log(Math.cbrt(27));    // Cube root of 27 = 3

// Min, max and random
console.log(Math.min(3, 1, 7, 9)); // Minimum number → 1
console.log(Math.max(3, 1, 7, 9)); // Maximum number → 9
console.log(Math.random());        // Random number between 0 and 1

// Logarithmic and exponential
console.log(Math.log(10));    // Natural log of 10
console.log(Math.log2(8));    // Log base 2 of 8 = 3
console.log(Math.log10(100)); // Log base 10 of 100 = 2
console.log(Math.exp(3));     // e^3

// Sign checking
console.log(Math.sign(10));  // 1 → positive
console.log(Math.sign(-5));  // -1 → negative
console.log(Math.sign(0));   // 0 → zero

// Mathematical constants
console.log(Math.PI);         // Value of π (3.141592653589793)
console.log(Math.E);          // Euler's number (2.718281828459045)
console.log(Math.SQRT2);      // Square root of 2
console.log(Math.SQRT1_2);    // Square root of 1/2
console.log(Math.LN2);        // Natural log of 2
console.log(Math.LN10);       // Natural log of 10
console.log(Math.LOG2E);      // Log base 2 of e
console.log(Math.LOG10E);     // Log base 10 of e